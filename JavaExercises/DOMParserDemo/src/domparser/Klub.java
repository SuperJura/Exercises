package domparser;

/**
 *
 * @author Jurica
 */
public class Klub {
    private String Ime;
    private String Grad;
    private float Proracun;
    private String Predsjednik;

    /**
     * @return the Ime
     */
    public String getIme() {
        return Ime;
    }

    /**
     * @param Ime the Ime to set
     */
    public void setIme(String Ime) {
        this.Ime = Ime;
    }

    /**
     * @return the Grad
     */
    public String getGrad() {
        return Grad;
    }

    /**
     * @param Grad the Grad to set
     */
    public void setGrad(String Grad) {
        this.Grad = Grad;
    }

    /**
     * @return the Proracun
     */
    public float getProracun() {
        return Proracun;
    }

    /**
     * @param Proracun the Proracun to set
     */
    public void setProracun(float Proracun) {
        this.Proracun = Proracun;
    }

    /**
     * @return the Predsjednik
     */
    public String getPredsjednik() {
        return Predsjednik;
    }

    /**
     * @param Predsjednik the Predsjednik to set
     */
    public void setPredsjednik(String Predsjednik) {
        this.Predsjednik = Predsjednik;
    }
}
